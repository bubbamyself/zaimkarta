const REGION_BY_CODE = new Map(
  RUSSIAN_REGIONS.map((region) => [region.code, region]),
);

export function normalizeRegionCode(code: string) {
  const trimmed = code.trim();

  if (!trimmed) {
    return null;
  }

  return trimmed.padStart(2, "0");
}

export function getRussianRegionByCode(code: string | null | undefined) {
  if (!code) {
    return null;
  }

  const normalizedCode = normalizeRegionCode(code);

  return normalizedCode ? REGION_BY_CODE.get(normalizedCode) ?? null : null;
}

export function isRussianRegionCode(code: string | null | undefined) {
  return Boolean(getRussianRegionByCode(code));
}

export function normalizeRegionCodes(codes: string[]) {
  const normalizedCodes = codes
    .map((code) => normalizeRegionCode(code))
    .filter((code): code is string => Boolean(code) && isRussianRegionCode(code));

  return Array.from(new Set(normalizedCodes));
}
